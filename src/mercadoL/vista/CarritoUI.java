    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Nodo<Zapatilla> nodoCarrito = Main.carrito;
        Cola colaHistorico = new Cola();
        int cantidad = 0;
        while (nodoCarrito != null) {
            Historico historico = new Historico(Long.toString(new Date().getTime()),
                     new Date(), 1, new Zapatilla(nodoCarrito.dato.getNombre(), nodoCarrito.dato.getPrecio(), null));
            colaHistorico.agregar(historico);
            
            cantidad ++;
            nodoCarrito = nodoCarrito.sig;
        }
        Main.carrito = null;
        Main.usuario.setHitoricoCompra(colaHistorico);
        
        DialogUI dialogUI = new DialogUI(this, true, "Compra de "+cantidad+" producto(s) \n realizada correctamente!!");
        dialogUI.setVisible(true);
        
        CatalogoUI catalogoui = new CatalogoUI();
        catalogoui.setVisible(true);
        catalogoui.pack();
        catalogoui.setLocationRelativeTo(null);
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed


