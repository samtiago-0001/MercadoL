    private void cargarCarrito() {
        DefaultTableModel modelo = new DefaultTableModel();

        modelo.addColumn("");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Total");

        agregarDatosTablaCarrito(modelo);
        jTable2.setModel(modelo);
    }

    private void agregarDatosTablaCarrito(DefaultTableModel modelo) {
        Double total = 0.0;
        Nodo<Zapatilla> nodoCarrito = Main.carrito;
        while (nodoCarrito != null) {
            modelo.addRow(new Object[]{nodoCarrito.dato.getNombre(), 1, formatoNumero(nodoCarrito.dato.getPrecio())});
            total = total + nodoCarrito.dato.getPrecio();
            nodoCarrito = nodoCarrito.sig;
        }

        jLabelTotal.setText(formatoNumero(total));
    }

    private String formatoNumero(Double cantidad) {
        NumberFormat formatoPais = NumberFormat.getCurrencyInstance(Locale.US);
        String numeroFormateado = formatoPais.format(cantidad);

        return numeroFormateado;
    }

